    private String capturarFecha(){
        String fecha;
          dia = Integer.toString(jDateChooser1.getCalendar().get(Calendar.DAY_OF_MONTH));
          mes = Integer.toString(jDateChooser1.getCalendar().get(Calendar.MONTH) + 1);
          agno = Integer.toString(jDateChooser1.getCalendar().get(Calendar.YEAR));
          
        fecha = (dia + "/" + mes + "/" + agno);
        return fecha;
    }
