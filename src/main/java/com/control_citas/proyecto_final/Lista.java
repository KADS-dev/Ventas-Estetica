    private void eliminarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarjButtonActionPerformed
        // TODO add your handling code here:
         if(jDateChooser1.getDate()==null){
            JOptionPane.showMessageDialog(null, "Por favor, selecciona una fecha");
            banderaFecha = false;
        }else{
            banderaFecha = true;
        }
        
        if(validarHoraUnica() == true){
            JOptionPane.showMessageDialog(null, "Por favor, ingresa una hora existente");
            banderaHora = false;
        }else{
        
            banderaHora = true;
                    
        }
                
       
        if(banderaFecha == true && banderaHora == true){            
            if(filas==2){
                eliminarUnicoDato();
            }else{
                eliminarDatos();  
            }
            
            
            jTable1.setModel(new DefaultTableModel(data, columnas));
            horajComboBox.setSelectedItem(" ");
            peinadojRadioButtonD.setSelected(true);
            tintejRadioButtonD.setSelected(true);
            manicurejRadioButtonD.setSelected(true);
            cortejComboBox.setSelectedItem("Default");
            cortejRadioButtonD.setSelected(true);
            nombrejTextField.setText("");
            telefonojTextField.setText("");
            correojTextField.setText("");
            domiciliojTextField.setText("");
            total=0;
            totalCorte = PRECIO_CORTED;
            totalPeinado = PRECIO_PEINADOD;
            totalTinte = PRECIO_TINTED;
            totalManicure = PRECIO_MANICURED;
            totalCorte = PRECIO_CORTED;
            totaljTextArea.setText("     -Peinado:\t\t"+totalPeinado+"\n\n"+"     -Tinte:\t\t"+totalTinte+"\n\n"+"     -Manicure:\t\t"+ totalManicure + "\n\n" + "     -Corte:\t\t"+totalCorte+"\n\n\n" + "   -Total:\t\t"+ total);
           
            dataCambio = data;
            ++filas;            
            data = new String[filas][11];
            regresarValoresAData(data, dataCambio);
            fechas[indiceFechas] = data;
        }
    }//GEN-LAST:event_eliminarjButtonActionPerformed

